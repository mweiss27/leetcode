/**
 * @param {character[][]} board
 * @param {string} word
 * @return {boolean}
 */
var exist = function (board, word) {
  const visitCurrent = (r, c, visited) => ({
    ...visited,
    [r]: {
      ...visited[r],
      [c]: 1,
    },
  })

  const isVisited = (r, c, visited) => visited[r] && visited[r][c]

  const foundWord = (r, c, w, visited = {}) => {
    if (r < 0 || r >= board.length || c < 0 || c >= board[0].length) {
      return false
    }

    if (board[r][c] === w[0] && !isVisited(r, c, visited)) {
      const remainingLetters = w.substring(1)
      if (!remainingLetters) {
        // There are no more letters, we've found the full word
        return true
      }

      // We need to see if we can find all remaining letters by looking around us
      const result =
        foundWord(r, c - 1, remainingLetters, visitCurrent(r, c, visited)) ||
        foundWord(r, c + 1, remainingLetters, visitCurrent(r, c, visited)) ||
        foundWord(r - 1, c, remainingLetters, visitCurrent(r, c, visited)) ||
        foundWord(r + 1, c, remainingLetters, visitCurrent(r, c, visited))

      return result
    }

    return false
  }

  for (let r = 0; r < board.length; r++) {
    for (let c = 0; c < board[0].length; c++) {
      // Look for the first letter of our word and kick off a search for the rest of the letters
      if (board[r][c] === word[0] && foundWord(r, c, word)) {
        return true
      }
    }
  }

  return false
}

